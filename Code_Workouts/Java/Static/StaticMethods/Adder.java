package Code_Workouts.JavaWorkouts.Static.StaticMethods;

public class Adder {
    public static int add(int lelement1, int lelement2) {
        return lelement1 + lelement2;
    }

    public static int add(int lelement1, int lelement2, int lelement3) {
        return lelement1 + lelement2 + lelement3;
    }

    public static double add(double lelement1, double lelement2) {
        return lelement1 + lelement2;
    }

    public int multiply(int lelement1, int lelement2) {
        return lelement1 * lelement2;
    }
}
