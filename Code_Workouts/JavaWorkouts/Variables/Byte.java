package Code_Workouts.JavaWorkouts.Variables;

public class Byte {
    public static void main(String[] args) {
        byte a = 10;
        byte b = 20;
        byte c = (byte) (a + b);
        System.out.println(c);
        int d = 130;
        byte e = (byte) d;
        System.out.println(e);
    }
}
