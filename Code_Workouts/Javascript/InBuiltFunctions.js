//Math
var a = Math.random();		// stores a random number between 0 and 1
a = a*6;					// To store a random number between 0 and 6
a = Math.floor(a);			// To make the random decimal number into a whole number
a = a+1;					// To get a number from 1 to 6 (can be used to get a random value for dice game)
console.log(a);
console.log(Math.floor(Math.random()*6)+1);	//The whole function can be rewritten like this